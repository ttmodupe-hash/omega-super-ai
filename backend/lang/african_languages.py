def get_all_african_codes() -> list[str]:
    """Return all African language codes."""
    return list(AFRICAN_LANGUAGES.keys())


def get_all_global_codes() -> list[str]:
    """Return all global language codes."""
    return list(GLOBAL_LANGUAGES.keys())


def get_all_codes() -> list[str]:
    """Return all language codes (African + Global)."""
    return get_all_african_codes() + get_all_global_codes()


def get_language_by_code(lang_code: str) -> dict | None:
    """Get language info by code."""
    return AFRICAN_LANGUAGES.get(lang_code) or GLOBAL_LANGUAGES.get(lang_code)


def get_regions() -> list[str]:
    """Get all unique African regions."""
    regions = set()
    for info in AFRICAN_LANGUAGES.values():
        regions.add(info["region"])
    return sorted(regions)


def get_language_families() -> list[str]:
    """Get all unique language families."""
    families = set()
    for info in {**AFRICAN_LANGUAGES, **GLOBAL_LANGUAGES}.values():
        families.add(info["language_family"])
    return sorted(families)


def get_countries() -> list[str]:
    """Get all unique countries."""
    countries = set()
    for info in {**AFRICAN_LANGUAGES, **GLOBAL_LANGUAGES}.values():
        countries.update(info["countries"])
    return sorted(countries)


def count_speakers(lang_code: str) -> int:
    """Parse speaker count string to integer."""
    info = get_language_by_code(lang_code)
    if not info:
        return 0
    speakers_str = info.get("speakers", "0")
    return int(speakers_str.replace(",", "").replace("+", ""))


